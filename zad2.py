def multiply(num1: int, num2: int) -> int:
    """
    Function multiplies 2 numbers.
    Params:
        num1 (int): 1st number to multiply
        num2 (int): 2nd number to multiply
    Returns:
        (int): result of multiplication
    """
    return num1 * num2


if __name__ == "__main__":
    print(multiply(3, 5))
